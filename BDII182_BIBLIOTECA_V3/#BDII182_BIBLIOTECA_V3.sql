use BDII182_BIBLIOTECA_V3;

create table CATEGORIAS (
ID int unsigned not null auto_increment,
CATEGORIA varchar (15) not null,
primary key (ID));

create table ESTADOS (
SIGLA_ESTADO VARCHAR (2) not null,
NOME_ESTADO varchar (25) not null,
primary key(SIGLA_ESTADO));

create table TIPOUSUARIOS(
ID int unsigned not null auto_increment,
TIPO_USUARIO varchar (15) not null,
MULTA_DIA double not null default '0',
PRAZO int unsigned not null,
primary key (ID));

create table USUARIOS (
ID int unsigned not null auto_increment,
NOME varchar (15) not null,
SOBRENOME varchar (15) not null,
ENDERECO varchar (45) not null,
CIDADE varchar (45) not null,
SIGLA_ESTADO varchar (2) not null,
TELEFONE varchar (11) not null,
TIPO_USUARIO varchar (15) not null,
primary key(ID),
constraint FK_ESTADOS_TIPOUSUARIOS

foreign key(SIGLA_ESTADO)
references ESTADOS(SIGLA_ESTADO),

foreign key(TIPO_USUARIO)
references TIPOUSUARIOS(TIPO_USUARIO)
);

create table LIVROS (
ID int unsigned not null auto_increment,
USUARIO_ID int unsigned default NULL,
TITULO varchar(45) not null,
ANO_PUBLICACAO int unsigned not null,
NUMERO_EDICAO int unsigned not null,
PAGINA int unsigned not null,
CATEGORIA_ID int unsigned default NULL,
PRECO double not null default '0',
primary key(ID),

constraint FK_LIVROS_USUARIOS_ESTADOS_CATEGORIAS
foreign key(USUARIO_ID)
references USUARIOS(ID),
foreign key(CATEGORIA_ID)
references CATEGORIAS(ID));



